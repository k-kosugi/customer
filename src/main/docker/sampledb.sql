CREATE TABLE CUSTOMER
(
  ID bigint,
  SSN char(25),
  NAME varchar(64),
  CONSTRAINT CUSTOMER_PK PRIMARY KEY(ID)
);
CREATE TABLE ADDRESS
(
  ID bigint,
  STREET char(25),
  ZIP char(10),
  CUSTOMER_ID bigint,
  CONSTRAINT ADDRESS_PK PRIMARY KEY(ID),
  CONSTRAINT CUSTOMER_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (ID)
);
